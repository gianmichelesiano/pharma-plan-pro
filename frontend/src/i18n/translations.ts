type Language = keyof typeof translations;
type Translations = typeof translations.it;
type Namespace = keyof Translations;

const LANG_ORDER: Language[] = ["it", "de", "fr", "en"];
const LANG_NAMES: Record<Language, string> = { it: "Italiano", de: "Deutsch", fr: "Français", en: "English" };
const LANG_FLAGS: Record<Language, string> = { it: "🇮🇹", de: "🇩🇪", fr: "🇫🇷", en: "🇬🇧" };

function nextLang(lang: Language): Language {
  const i = LANG_ORDER.indexOf(lang);
  return LANG_ORDER[(i + 1) % LANG_ORDER.length];
}

